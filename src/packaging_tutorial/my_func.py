def my_add(x1: float, x2: float) -> float:
    """ 足し算を行う (x1 + x2)

    Argumentns:
        :x1: float
            数値1

        :x2: float
            数値2

    Returns:
        :y: float
            x1 + x2
    """

    y = x1 + x2
    return y


def my_sub(x1: float, x2: float) -> float:
    """ 引き算を行う (x1 + x2)

    Argumentns:
        :x1: float
            数値1

        :x2: float
            数値2

    Returns:
        :y: float
            x1 - x2
    """

    y = x1 - x2
    return y


def my_mul(x1: float, x2: float) -> float:
    """ 掛け算を行う (x1 * x2)

    Argumentns:
        :x1: float
            数値1

        :x2: float
            数値2

    Returns:
        :y: float
            x1 * x2
    """

    y = x1 * x2
    return y
